  private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed

    int linhaSelecionada = tb_beneficiario.getSelectedRow();
    if (linhaSelecionada == -1) {
      JOptionPane.showMessageDialog(null, "Selecione uma linha para apagar!", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
    }
    int answer = JOptionPane.showConfirmDialog(null, "Tens certeza?", "Aviso", JOptionPane.WARNING_MESSAGE);
    if (answer == 0) {
      try {
        Beneficiario beneEncontrado = listaBeneficiarios.get(linhaSelecionada);
        beneficarioController.excluir(beneEncontrado.getId_beneficiario());

        listarDados();
      } catch (ClassNotFoundException | SQLException ex) {
        System.getLogger(BeneficiarioPanelView.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
      } finally {
        tb_beneficiario.clearSelection();
      }
    }
  }//GEN-LAST:event_btn_EliminarActionPerformed

  private void btn_Editar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Editar1ActionPerformed

    int linhaSelecionada = tb_beneficiario.getSelectedRow();
    if (linhaSelecionada == -1) {
      JOptionPane.showMessageDialog(null, "Selecione uma linha para Editar!", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
    }

    java.awt.Frame parent = (java.awt.Frame) javax.swing.SwingUtilities.getWindowAncestor(this);
    EditarBeneficiarioDialog dialog = new EditarBeneficiarioDialog(parent, true, this);

    Beneficiario beParaEditar = listaBeneficiarios.get(linhaSelecionada);

    dialog.setBeneficiarioForEdicao(beParaEditar);
    dialog.setVisible(true);
  }//GEN-LAST:event_btn_Editar1ActionPerformed


