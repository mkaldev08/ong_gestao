  private void btn_Editar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Editar1ActionPerformed

    int linhaSelecionada = tb_projeto.getSelectedRow();
    if (linhaSelecionada == -1) {
      JOptionPane.showMessageDialog(null, "Selecione uma linha para Editar!", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
    }

    java.awt.Frame parent = (java.awt.Frame) javax.swing.SwingUtilities.getWindowAncestor(this);
    EditarProjetoDialog dialog = new EditarProjetoDialog(parent, true, this);

    Projeto projParaEditar = listaProjetos.get(linhaSelecionada);

    dialog.setProtejoForEdicao(projParaEditar);
    dialog.setVisible(true);
  }//GEN-LAST:event_btn_Editar1ActionPerformed

  public void listarDados() {

    tabelaModelo.setRowCount(0);
    try {
      this.listaProjetos = projetoController.listar();
    } catch (ClassNotFoundException ex) {
      System.getLogger(ProjetoPanelView.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
    } catch (SQLException ex) {
      System.getLogger(ProjetoPanelView.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
    }
    for (Projeto p : listaProjetos) {

      tabelaModelo.addRow(new Object[]{
        p.getNome(),
        p.getDescricao(),
        p.getData_criacao().toString(),
        p.getData_fim() != null ? p.getData_fim().toString() : "em andamento",
        p.getStatus()
      });
    }
  }

