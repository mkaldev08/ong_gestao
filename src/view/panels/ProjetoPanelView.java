  private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed

    int linhaSelecionada = tb_projeto.getSelectedRow();
    if (linhaSelecionada == -1) {
      JOptionPane.showMessageDialog(null, "Selecione uma linha para apagar!", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
    }
    int answer = JOptionPane.showConfirmDialog(null, "Tens certeza?", "Aviso", JOptionPane.WARNING_MESSAGE);
    if (answer == 0) {
      try {
        Projeto projEncontrado = listaProjetos.get(linhaSelecionada);
        projetoController.excluir(projEncontrado.getId_projeto());

        listarDados();
      } catch (ClassNotFoundException | SQLException ex) {
        System.getLogger(ProjetoPanelView.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
      } finally {
        tb_projeto.clearSelection();
      }
    }
  }//GEN-LAST:event_btn_EliminarActionPerformed

