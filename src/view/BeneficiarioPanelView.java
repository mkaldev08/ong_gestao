  private void listarDados() {
    try {
      tabelaModelo.setRowCount(0);

      List<Beneficiario> listaBeneficiarios = beneficarioController.listar();

      for (Beneficiario b : listaBeneficiarios) {

        tabelaModelo.addRow(new Object[]{
          b.getNome(),
          b.getTipo(),
          b.getDocumento(),
          b.getTelefone(),
          b.isAtivo() ? "Sim" : "NÃ£o"
        });
      }
    } catch (ClassNotFoundException | SQLException ex) {
      System.getLogger(BeneficiarioPanelView.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
    }
  }

